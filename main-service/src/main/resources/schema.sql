CREATE TABLE IF NOT EXISTS users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  email VARCHAR(254) NOT NULL,
  name VARCHAR(250) NOT NULL,
  CONSTRAINT uq_user_email UNIQUE (email)
);

CREATE TABLE IF NOT EXISTS categories (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(50) NOT NULL,
  CONSTRAINT uq_category_name UNIQUE (name)
);

CREATE TABLE IF NOT EXISTS events (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  annotation VARCHAR(2000) NOT NULL,
  category_id BIGINT NOT NULL,
  confirmed_requests INTEGER NOT NULL DEFAULT 0,
  created_on TIMESTAMP WITHOUT TIME ZONE NOT NULL,
  description VARCHAR(7000) NOT NULL,
  event_date TIMESTAMP WITHOUT TIME ZONE NOT NULL,
  initiator_id BIGINT NOT NULL,
  lat DOUBLE PRECISION NOT NULL,
  lon DOUBLE PRECISION NOT NULL,
  paid BOOLEAN NOT NULL DEFAULT FALSE,
  participant_limit INTEGER NOT NULL DEFAULT 0,
  published_on TIMESTAMP WITHOUT TIME ZONE,
  request_moderation BOOLEAN NOT NULL DEFAULT TRUE,
  state VARCHAR(20) NOT NULL CHECK (state IN ('PENDING','PUBLISHED', 'CANCELED')),
  title VARCHAR(120) NOT NULL,
  views BIGINT NOT NULL DEFAULT 0,
  comments BIGINT NOT NULL DEFAULT 0,
  CONSTRAINT fk_events_to_categories FOREIGN KEY(category_id) REFERENCES categories(id),
  CONSTRAINT fk_events_to_users FOREIGN KEY(initiator_id) REFERENCES users(id)
);

CREATE TABLE IF NOT EXISTS compilations (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  pinned BOOLEAN NOT NULL DEFAULT FALSE,
  title VARCHAR(50) NOT NULL,
  CONSTRAINT uq_compilations_title UNIQUE (title)
);

CREATE TABLE IF NOT EXISTS compilation_events (
 compilation_id BIGINT NOT NULL,
 event_id BIGINT NOT NULL,
 CONSTRAINT pk_compilation_events PRIMARY KEY (compilation_id, event_id),
 CONSTRAINT fk_compilation_events_to_compilations
 FOREIGN KEY(compilation_id) REFERENCES compilations(id) ON DELETE CASCADE,
 CONSTRAINT fk_compilation_events_to_events FOREIGN KEY(event_id) REFERENCES events(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS participation_requests (
 id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
 created TIMESTAMP WITHOUT TIME ZONE NOT NULL,
 event_id BIGINT NOT NULL,
 requester_id BIGINT NOT NULL,
 status VARCHAR(20) NOT NULL CHECK (status IN ('PENDING','CONFIRMED','REJECTED','CANCELED')),
 CONSTRAINT fk_participation_requests_to_events FOREIGN KEY(event_id) REFERENCES events(id) ON DELETE CASCADE,
 CONSTRAINT fk_participation_requests_to_users FOREIGN KEY(requester_id) REFERENCES users(id)
);

CREATE TABLE IF NOT EXISTS comments (
 id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
 text VARCHAR(3000) NOT NULL,
 author_id BIGINT NOT NULL,
 event_id BIGINT NOT NULL,
 created TIMESTAMP WITHOUT TIME ZONE NOT NULL,
 updated TIMESTAMP WITHOUT TIME ZONE,
 CONSTRAINT fk_comments_to_users FOREIGN KEY(author_id) REFERENCES users(id),
 CONSTRAINT fk_comments_to_events FOREIGN KEY(event_id) REFERENCES events(id) ON DELETE CASCADE
);